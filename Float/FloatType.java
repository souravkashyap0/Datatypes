class FloatType
{
	public static void main(String [] args)
	{
		float rateOfInterest=10.5f;// The rate of interest a person need to pay if he or she has taken a loan in a bank
		System.out.println("The rate of interest in a bank is :" + rateOfInterest);
		
		float pi= 3.14f;// When assigning a value for pi with fixed precision,for more than 7 digit after point or decimal we use DOUBLE
		System.out.println("The constant PI value is:"+ pi);
		
		float measurements =21.7f;//The measurements from experiments,other real world examples
		System.out.println("The value is "+ measurements);
	
		float propertySize =500.5f;//The size of a property
		System.out.println("The size of property"+ propertySize);
	
	}
}