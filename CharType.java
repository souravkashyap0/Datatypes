class CharType
{
	public static void main(String [] args)
	{
		char gender='M';// The gender of a person
		System.out.println("The gender of a person is  :" + gender);
		
		char character='A';//Can be used to get the ASCII number of a character
		System.out.println("The ASCII number is :"+ character);
		
		char unicode ='\u005A';//The way to represent character into unicode system
		System.out.println("The character is "+ unicode);
	
	
	}
}